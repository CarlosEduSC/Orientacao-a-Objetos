public class CarroEletrico extends Carro {
    private float voltagemBateria;
    private float horasBateria;

    public float getVoltagemBateria() {
        return this.voltagemBateria;
    }

    public void setVoltagemBateria(float v) {
        this.voltagemBateria = v;
    }

    public float getHorasBateria() {
        return this.horasBateria;
    }

    public void setHorasBateria(float t) {
        this.horasBateria = t;
    }
}
