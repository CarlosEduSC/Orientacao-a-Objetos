public class Triciculo extends Bicicleta {
    private String tipoBanco;
    private String corBanco;

    public String getTipoBanco() {
        return this.tipoBanco;
    }

    public void setTipoBanco(String t) {
        this.tipoBanco = t;
    }

    public String getCorBanco() {
        return this.corBanco;
    }

    public void setCorBanco(String c) {
        this.corBanco = c;
    }
}
